package async

import (
	"context"
	"sync"
)

// tests: ✅
// bench: ✅
func AsyncSemaphore[A any, V any](ctx context.Context, args []A, f func(context.Context, int, A) (V, error), concurrency int) ([]V, error) {
	if concurrency == 0 {
		concurrency = len(args)
	}

	traffic := make(chan struct{}, concurrency)
	output := make(chan struct {
		Index int
		Value V
		error
	}, concurrency) // size=concurrency to prevent blocking of the input channel

	wg := sync.WaitGroup{}
	res := make([]V, len(args))

	go func() {
		ctx, cancel := context.WithCancel(ctx)
		defer cancel()

		defer func() {
			close(traffic)
			wg.Wait()
			close(output)
		}()

		for i, arg := range args {
			i, arg := i, arg

			select {
			case traffic <- struct{}{}:
			case <-ctx.Done():
				return
			}

			wg.Add(1)
			go func() {
				defer wg.Done()
				defer func() { <-traffic }()
				value, err := f(ctx, i, arg)
				output <- struct {
					Index int
					Value V
					error
				}{i, value, err}
				if err != nil {
					cancel()
				}
			}()
		}
	}()

	for {
		select {
		case <-ctx.Done():
			return res, ctx.Err()
		case msg, ok := <-output:
			if !ok {
				return res, nil
			}
			if msg.error != nil {
				return res, msg.error
			}
			res[msg.Index] = msg.Value
		}
	}
}
